class CharDataType {
  public static void main(String[] args) {
    	
    char letter = '\u0041';                //The minimum value of the char data type is '\u0000' (0) and the maximum value of the is '\uffff'.
    System.out.println(letter);            // Default value: '\u0000'

    char letter1 = '9';
    System.out.println(letter1);           // prints 9
    	
    char letter2 = 65;
    System.out.println(letter2);            // prints A
  }
}
